<?php
// Определяем многомерный массив с информацией о книгах в библиотеке
$library = array(
    array("название" => "Книга 1", "автор" => "Автор 1", "жанр" => "Фантастика"),
    array("название" => "Книга 2", "автор" => "Автор 2", "жанр" => "Детектив"),
    array("название" => "Книга 3", "автор" => "Автор 3", "жанр" => "Классика"),
    array("название" => "Книга 4", "автор" => "Автор 4", "жанр" => "Фэнтези"),
    array("название" => "Книга 5", "автор" => "Автор 5", "жанр" => "Детектив")
);

// Жанр книг, которые мы ищем
$genre = "Детектив";

// Находим все книги указанного жанра
echo "Книги жанра '{$genre}':\n";
foreach ($library as $book) {
    if ($book['жанр'] == $genre) {
        echo $book['название'] . " - " . $book['автор'] . "\n";
    }
}
